    this.depnRateFholdProp = client.depnRateFholdProp;
    this.depnBasisShortLhold = client.depnBasisShortLhold;
    this.depnRateShortLhold = client.depnRateShortLhold;
    this.depnBasisLongLhold = client.depnBasisLongLhold;
    this.depnRateLongLhold = client.depnRateLongLhold;
    this.depnBasisImprovements = client.depnBasisImprovements;
    this.depnRateImprovements = client.depnRateImprovements;
