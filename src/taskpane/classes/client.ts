    this.depnRateCompEquip = client.depnRateCompEquip;
    this.depnBasisOfficeEquip = client.depnBasisOfficeEquip;
    this.depnRateOfficeEquip = client.depnRateOfficeEquip;
    this.depnBasisIPOwned = client.depnBasisIPOwned;
    this.depnRateIPOwned = client.depnRateIPOwned;
