    this.clientName = client.clientName;
    this.companyNumber = client.companyNumber;
    this.incorpDate = client.incorpDate;
    this.accRefMonth = client.accRefMonth;
    this.accRefDate = client.accRefDate;
    this.nominatedDay = client.nominatedDay;
